            <a name="theplan"></a>
            <h4>The Plan</h4>
            <p>These are the step-by-step instructions to become a multi-millionaire. Do these in order. Don’t move to the next one until you finish the previous.</p>
            <div class="card plan">
                <div class="card-header" id="headingP1" data-toggle="collapse" data-target="#collapseP1" aria-expanded="true" aria-controls="collapseP1">
                     1. Save one month worth of expenses
                </div>
                <div id="collapseP1" class="collapse" aria-labelledby="headingP1">
                  <div class="card-body">
                    <ul>
                        <li><strong>Being broke is expensive:</strong> Overdraft fees, credit card interest, 
                        payday loans, utility and rent deposits, etc. Getting out of this trap is the first step to becoming wealthy.
                        <li><strong>Most Americans are broke: </strong>
                        <a href="https://money.cnn.com/2011/08/10/pf/emergency_fund/">64% of Americans
                        can't afford a $1,000 emergency expense</a>. 
                        </li>
                    </ul>
                  </div>
                </div>
            </div> 
            <div class="card plan">
                <div class="card-header" id="headingP2" data-toggle="collapse" data-target="#collapseP2" aria-expanded="true" aria-controls="collapseP2">
                     2. Contribute to your 401k/403b up to match
                </div>
                <div id="collapseP2" class="collapse" aria-labelledby="headingP2">
                  <div class="card-body">
                    <ul>
                        <li><strong>Employer sponsored:</strong> Some employers offer a 401k or 403b retirement investment account. Not all do. If your employer doesn't offer one, skip this step.</li>
                        <li><strong>Some match:</strong> Some employers who offer a 401k or 403b will contribute money to YOUR account on your behaf ONLY IF you also contribute. If your employer doesn't match, skip this step.
                        <li><strong>Free money:</strong> This match represents an instant 100% return on your money. Not making this contribution is essentially throwing away money. 
                    </ul>
                  </div>
                </div>
            </div> 
            <div class="card plan">
                <div class="card-header" id="headingP3" data-toggle="collapse" data-target="#collapseP3" aria-expanded="true" aria-controls="collapseP3">
                     3. Aggressively pay off all debt (except mortgage)
                </div>
                <div id="collapseP3" class="collapse" aria-labelledby="headingP3">
                  <div class="card-body">
                    <ul>
                        <li><strong>Seriously:</strong> Yes, seriously. Pay off ALL of your debt except your mortgage. And never use consumer debt again.</li>
                        <li><strong>No payments:</strong> When you have <i>no payments</i> on stuff you bought in the past, you can use the full power of your income to invest and build wealth.</li>
                        <li><strong>Down escalator:</strong> Trying to invest and build wealth while still
                        in debt is like running up the down escalator. Get rid of the debt first, then put your full focus on building wealth.</li>
                    </ul>
                  </div>
                </div>
            </div> 
            <div class="card plan">
                <div class="card-header" id="headingP4" data-toggle="collapse" data-target="#collapseP4" aria-expanded="true" aria-controls="collapseP4">
                     4. Save full emergency fund (3-6 months expenses)
                </div>
                <div id="collapseP4" class="collapse" aria-labelledby="headingP4">
                  <div class="card-body">
                    <ul>
                        <li><strong>Do not invest:</strong> Put this money in a savings or money market account
                            separate from your checking account where you pay your bills.
                        </li>
                        <li><strong>Forget about it:</strong> Forget about it and move to the next step. When you have an actual emergency
                        you'll know you need it.</li>
                        <li><strong>Not-poor insurance:</strong>  Your emergency fund does the important job of acting as
                            "not poor insurance". When you have an emergency, you don't want to fall back to the gauntlet of being poor 
                            (overdraft fees, credit card interest, payday loans, etc.) By leaving your emergency fund in cash it's helping
                            to build your future wealth by not letting yourself fall back into the trap of being poor.
                    </ul>
                  </div>
                </div>
            </div> 
